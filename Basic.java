public class Basic {
    public static void main(String[] args) {
        // if condition
        int a = 10;
        if (a == 10) {
            System.out.println("hello world");
        }

        // while loop
        int count = 1;
        while (count != 5) {
            System.out.println(count);
            count++;
        }

        //for loop
        
         for(int iteration = 1 ; iteration != 5 ; iteration++){
              System.out.println(iteration);
         }

    }
}
